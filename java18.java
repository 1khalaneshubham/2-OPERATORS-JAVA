class Demo{
	public static void main(String[] args){

		int x = 132;
		int y = 75;

		System.out.println(x >> 5);  // 4
		System.out.println(x >> 1);  // 66
		System.out.println(x >> 2);  // 33
		System.out.println(x << 2);  // 528
		System.out.println(x << 3);  // 1056

		System.out.println(y >> 5);  // 2
		System.out.println(y >> 1);  // 37
		System.out.println(y >> 2);  // 18
		System.out.println(y << 2);  // 300
		System.out.println(y << 3);  // 600
	}
	
}

